const QUESTION = FONT['?'];

export function pixelText(
  ctx: CanvasRenderingContext2D,
  str: string,
  x: number,
  y: number,
  color: string
) {
  const s = String(str).toUpperCase();
  ctx.fillStyle = color;
  for (let i = 0; i < s.length; i += 1) {
    const ch = s[i];
    const glyph = (ch && FONT[ch]) || QUESTION;
    if (!glyph) continue;
    for (let yy = 0; yy < 7; yy += 1) {
      const row = glyph[yy];
      if (!row) continue;
      for (let xx = 0; xx < 5; xx += 1) {
        if (row[xx] === '#') ctx.fillRect(x + i * 6 + xx, y + yy, 1, 1);
      }
    }
  }
}

export function drawBigText(
  ctx: CanvasRenderingContext2D,
  str: string,
  x: number,
  y: number,
  color: string
) {
  const s = String(str).toUpperCase();
  ctx.fillStyle = color;
  for (let i = 0; i < s.length; i += 1) {
    const ch = s[i];
    const glyph = (ch && FONT[ch]) || QUESTION;
    if (!glyph) continue;
    for (let yy = 0; yy < 7; yy += 1) {
      const row = glyph[yy];
      if (!row) continue;
      for (let xx = 0; xx < 5; xx += 1) {
        if (row[xx] === '#') {
          ctx.fillRect(x + i * 12 + xx * 2, y + yy * 2, 2, 2);
        }
      }
    }
  }
}

export function drawRing(
  ctx: CanvasRenderingContext2D,
  cx: number,
  cy: number,
  r: number,
  sym: string,
  ringColor: string,
  coreColor: string
) {
  ctx.fillStyle = ringColor;
  for (let a = 0; a < 360; a += 10) {
    const rad = (a * Math.PI) / 180;
    const x = Math.round(cx + Math.cos(rad) * r);
    const y = Math.round(cy + Math.sin(rad) * r);
    ctx.fillRect(x, y, 1, 1);
  }
  if (r >= 4 && sym) {
    pixelText(ctx, sym, cx - sym.length * 3 + 1, cy - 3, coreColor);
  }
}

